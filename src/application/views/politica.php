		<h2>Política</h2>
		<p>&nbsp;</p>
		<p>1. Como primer paso para realizar compras a través de la Tienda Virtual de la UPC, el usuario debe registrar sus principales datos a través de la opción REGÍSTRESE.</p>
	<p>&nbsp;</p>
		<p>2. La Tienda Virtual de la UPC cuenta con la Certificación Internacional (Internet Secure Socket Layer – SSL) que garantiza la confidencialidad de los datos del usuario, así como la seguridad de las transacciones. Dicha certificación se aplica tanto para accesos desde Internet Explorer como Netscape Navigator.</p>
		<p>&nbsp;</p>
	<p>3. Para registrarse, el usuario debe colocar el país de residencia, es decir el lugar en el que se encuentra al momento de realizar su compra.</p>
	<p>&nbsp;</p>
		<p>4. Si el usuario ya se registró y desea volver a comprar, sólo debe indicar la Clave de Acceso que colocó en la opción REGÍSTRESE.	</p>
	<p>&nbsp;</p>
		<p>5. La Tienda Virtual de la UPC requiere que el usuario se identifique con su nombre de usuario y password antes de poder acceder a su información. Si olvida estos datos, el usuario puede utilizar la facilidad de RECORDATORIO.</p>
	<p>&nbsp;</p>
		<p>6. Cuando el usuario realice su compra, sus datos serán enviados automáticamente al servidor de comercio seguro de la UPC, protegiendo toda su información personal antes de ser transmitida por Internet.</p>
	<p>&nbsp;</p>
		<p>7. La Tienda Virtual de la UPC se reserva el derecho de modificar o aumentar cualquiera de las condiciones de acceso y uso del servicio. La información sobre dichos cambios estará disponible para el usuario en el sitio en el WWW de la Tienda Virtual de la UPC.</p>
	<p>&nbsp;</p>
		<p>8. La Tienda Virtual de la UPC se reserva el derecho de interrumpir su servicio, sea en forma transitoria o permanente, sin previo aviso o consentimiento de sus usuarios.</p>
	<p>&nbsp;</p>
		<p>9. El usuario puede estar comprando en la Tienda Virtual de la UPC alguna publicación o servicio que no tenga stock o cupo disponible al momento de armar su pedido. Pudiera suceder debido al movimiento del stock o cupo por los distintos puntos de distribución de la UPC. En este caso, el usuario puede indicar en Observaciones alguna de las opciones precisadas en la Política de Cambios y Devoluciones.</p>
	<p>&nbsp;</p>
		<p>10. El horario de atención para el ingreso de un pedido a la Tienda Virtual de la UPC es de 24 horas, los 7 días de la semana. El tiempo de entrega de los pedidos está detallado en la Política de Entrega.</p>
		<p>&nbsp;</p>
		<p>&nbsp;</p>
		<p>11. Los artículos en oferta en la Tienda Virtual de la UPC son los mismos que se ofrecen en el campus de la Universidad y tienen la misma duración de tiempo. Los detalles de la oferta estarán disponibles para el usuario en el sitio en el WWW de la Tienda Virtual de la UPC.</p>
