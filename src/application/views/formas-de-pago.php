		  <h2>¿Que es PayPal?</h2>
		<p>&nbsp;</p>
		<p>PayPal es un servicio sin costo que transfiere el pago de su tarjeta de crédito al comercio PayPal sin compartir su información financiera.<br />
		  </p>
		<p>&nbsp;</p>
		<h2>¿Cómo funciona?</h2>
	<p>&nbsp;</p>
		<p> Paso 1 : Selecciona el botón PayPal al escoger la forma de pago.<br />
		  Paso 2: Inicie sesión en PayPal. Simplemente ingrese su dirección de correo electrónico y contraseña.<br />
    Paso 3: Confirme su compra con solo un clic. Jamás necesitará ingresar la información de su tarjeta de crédito.¡Ha terminado! Fácil. Rápido. Seguro.</p>
